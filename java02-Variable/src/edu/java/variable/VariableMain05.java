package edu.java.variable;

public class VariableMain05 {

	public static void main(String[] args) { 
		
		System.out.println("논리형 자료 타입(boolean)");
		// boolean : 참(true) 또는 거짓(false)만을 저장하는 타입  
		
		boolean b1 = true; 
		System.out.println("b1 = " + b1);
		
		boolean b2 = false; 
		System.out.println("b2 = " + b2);
		
		boolean b3 = 1 < 0; 
		System.out.println("b3 = " + b3);
		
	}
	
} // end VariableMain05
